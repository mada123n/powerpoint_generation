# ANIMATION XML
# ──────────────────────────────────────────────────────────────────────────────

def build_animation_xml(reveal_shape_ids: list) -> str:
    """
    Generate <p:timing> XML for click-to-reveal animations.
    Each shape in reveal_shape_ids starts hidden and appears on individual clicks.
    """
    # Initial hide animations (fire at slide start, delay=0)
    initial_hides = []
    for i, sid in enumerate(reveal_shape_ids):
        ctn_id = 10 + i
        initial_hides.append(
            f'                <p:set>\n'
            f'                  <p:cBhvr override="childStyle">\n'
            f'                    <p:cTn id="{ctn_id}" dur="1" fill="hold"/>\n'
            f'                    <p:tgtEl><p:spTgt spid="{sid}"/></p:tgtEl>\n'
            f'                    <p:attrNameLst>\n'
            f'                      <p:attrName>style.visibility</p:attrName>\n'
            f'                    </p:attrNameLst>\n'
            f'                  </p:cBhvr>\n'
            f'                  <p:to><p:strVal val="hidden"/></p:to>\n'
            f'                </p:set>'
        )

    # Click-reveal animations (main sequence, one click per box)
    click_effects = []
    base = 50
    for i, sid in enumerate(reveal_shape_ids):
        p1  = base + i * 10
        p2  = p1 + 1
        p3  = p1 + 2
        click_effects.append(
            f'                <p:par>\n'
            f'                  <p:cTn id="{p1}" fill="hold">\n'
            f'                    <p:stCondLst><p:cond evt="onBegin" delay="indefinite"/></p:stCondLst>\n'
            f'                    <p:childTnLst>\n'
            f'                      <p:par>\n'
            f'                        <p:cTn id="{p2}" presetID="1" presetClass="entr" presetSubtype="0"\n'
            f'                               fill="hold" grpId="{i}" nodeType="clickEffect">\n'
            f'                          <p:stCondLst><p:cond evt="begin" delay="0"/></p:stCondLst>\n'
            f'                          <p:childTnLst>\n'
            f'                            <p:set>\n'
            f'                              <p:cBhvr>\n'
            f'                                <p:cTn id="{p3}" dur="1" fill="hold"/>\n'
            f'                                <p:tgtEl><p:spTgt spid="{sid}"/></p:tgtEl>\n'
            f'                                <p:attrNameLst>\n'
            f'                                  <p:attrName>style.visibility</p:attrName>\n'
            f'                                </p:attrNameLst>\n'
            f'                              </p:cBhvr>\n'
            f'                              <p:to><p:strVal val="visible"/></p:to>\n'
            f'                            </p:set>\n'
            f'                          </p:childTnLst>\n'
            f'                        </p:cTn>\n'
            f'                      </p:par>\n'
            f'                    </p:childTnLst>\n'
            f'                  </p:cTn>\n'
            f'                </p:par>'
        )

    build_items = "\n".join(
        f'    <p:bldP spid="{sid}" grpId="{i}" uiExpand="1"/>'
        for i, sid in enumerate(reveal_shape_ids)
    )

    return (
        f'  <p:timing>\n'
        f'    <p:tnLst>\n'
        f'      <p:par>\n'
        f'        <p:cTn id="1" dur="indefinite" restart="whenNotActive" nodeType="tmRoot">\n'
        f'          <p:childTnLst>\n'
        f'            <p:par>\n'
        f'              <p:cTn id="2" fill="hold">\n'
        f'                <p:stCondLst><p:cond delay="0"/></p:stCondLst>\n'
        f'                <p:childTnLst>\n'
        + "\n".join(initial_hides) + "\n"
        f'                </p:childTnLst>\n'
        f'              </p:cTn>\n'
        f'            </p:par>\n'
        f'            <p:seq concurrent="1" nextAc="seek">\n'
        f'              <p:cTn id="3" dur="indefinite" nodeType="mainSeq">\n'
        f'                <p:childTnLst>\n'
        + "\n".join(click_effects) + "\n"
        f'                </p:childTnLst>\n'
        f'              </p:cTn>\n'
        f'              <p:prevCondLst><p:cond evt="onPrevMouseUp" delay="0"/></p:prevCondLst>\n'
        f'            </p:seq>\n'
        f'          </p:childTnLst>\n'
        f'        </p:cTn>\n'
        f'      </p:par>\n'
        f'    </p:tnLst>\n'
        f'    <p:bldLst>\n'
        + build_items + "\n"
        f'    </p:bldLst>\n'
        f'  </p:timing>'
    )


