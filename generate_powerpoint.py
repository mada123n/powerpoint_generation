def build_powerpoint(content: dict, output_path: Path) -> None:
    """
    Main assembly function.
    1. Unpack Palette.pptx
    2. Duplicate the right template slide for each output slide
    3. Rewrite sldIdLst (remove templates, keep only output slides)
    4. Fill content in each duplicated slide
    5. Clean orphaned files (removes original 13 template slides)
    6. Pack to output .pptx
    """
    work_dir = Path(tempfile.mkdtemp(prefix="pptx_gen_"))
    print(f"\n  Working dir: {work_dir}")

    try:
        # ── Step 1: Unpack Palette.pptx ──────────────────────────────────────
        print("  📦 Unpacking Palette.pptx …")
        pptx_unpack(PALETTE_PATH, work_dir)

        slides_dir = work_dir / "ppt" / "slides"

        # Read initial max slide ID once (before any duplication changes presentation.xml)
        pres_xml_text = (work_dir / "ppt" / "presentation.xml").read_text(encoding="utf-8")
        existing_sld_ids = [
            int(m) for m in re.findall(r'<p:sldId[^>]*id="(\d+)"', pres_xml_text)
        ]
        next_slide_id = max(existing_sld_ids) + 1 if existing_sld_ids else 256

        # ── Step 2: Duplicate template slides ────────────────────────────────
        print(f"  🔄 Duplicating {len(content['slides'])} slides …")
        output_info: list[tuple[str, int, str, dict]] = []  # (filename, sid, rid, slide_dict)

        for slide in content["slides"]:
            slide_type    = slide.get("type", "")
            template_file = SLIDE_VARIANTS.get(slide_type)
            if template_file is None:
                print(f"    ⚠  Unknown type '{slide_type}' – skipping")
                continue

            dest_name, new_rid = duplicate_slide(work_dir, template_file, next_slide_id)
            output_info.append((dest_name, next_slide_id, new_rid, slide))
            print(f"    Slide {slide.get('slide_number', '?'):2}  "
                  f"[{slide_type}]  →  {dest_name}  (id={next_slide_id})")
            next_slide_id += 1

        # ── Step 3: Rewrite sldIdLst ──────────────────────────────────────────
        print("  📋 Updating slide order …")
        update_slide_id_list(
            work_dir,
            [(sid, rid) for _, sid, rid, _ in output_info]
        )

        # ── Step 4: Fill in slide content ─────────────────────────────────────
        print("  ✏️  Writing slide content …")
        for dest_name, _, _, slide in output_info:
            slide_path = slides_dir / dest_name
            print(f"    Slide {slide.get('slide_number', '?'):2}  "
                  f"[{slide.get('type', '')}]")
            apply_slide_content(slide_path, slide)

        # ── Step 5: Clean orphaned files (removes template slides 1–13) ───────
        print("  🧹 Cleaning …")
        pptx_clean(work_dir)

        # ── Step 6: Pack to output ────────────────────────────────────────────
        print(f"  📦 Packing → {output_path.name} …")
        pptx_pack(work_dir, output_path)

        print(f"\n  ✅ Saved: {output_path}")

    finally:
        shutil.rmtree(work_dir, ignore_errors=True)


