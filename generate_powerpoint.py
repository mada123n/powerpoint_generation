def para_title(text: str) -> str:
    """Simple paragraph for title placeholders (inherits master formatting)."""
    return (
        f'          <a:p>\n'
        f'            <a:r>\n'
        f'              <a:rPr lang="en-GB" dirty="0"/>\n'
        f'              <a:t>{xml_escape(text)}</a:t>\n'
        f'            </a:r>\n'
        f'          </a:p>'
    )


def para_bullet(text: str) -> str:
    """Bullet point paragraph matching Palette.pptx body placeholder format.
    Font is inherited from the slide master (Calisto MT Body) — no explicit
    typeface declaration so the master theme font applies automatically."""
    return (
        f'          <a:p>\n'
        f'            <a:pPr marL="342900" indent="-342900">\n'
        f'              <a:spcAft><a:spcPts val="400"/></a:spcAft>\n'
        f'              <a:buSzPct val="100000"/>\n'
        f'            </a:pPr>\n'
        f'            <a:r>\n'
        f'              <a:rPr lang="en-GB" dirty="0">\n'
        f'                <a:solidFill><a:srgbClr val="1A1A2E"/></a:solidFill>\n'
        f'              </a:rPr>\n'
        f'              <a:t>{xml_escape(text)}</a:t>\n'
        f'            </a:r>\n'
        f'          </a:p>'
    )


def para_text(text: str, bold: bool = False, italic: bool = False,
              color_hex: str = "1A1A2E", scheme_color: str = None) -> str:
    """Plain paragraph for text boxes (definition box, example cards, etc.)."""
    b_attr = ' b="1"' if bold else ''
    i_attr = ' i="1"' if italic else ''

    if scheme_color:
        fill_xml = f'<a:solidFill><a:schemeClr val="{scheme_color}"/></a:solidFill>'
    else:
        fill_xml = f'<a:solidFill><a:srgbClr val="{color_hex}"/></a:solidFill>'

    return (
        f'          <a:p>\n'
        f'            <a:r>\n'
        f'              <a:rPr lang="en-GB"{b_attr}{i_attr} dirty="0">\n'
        f'                {fill_xml}\n'
        f'              </a:rPr>\n'
        f'              <a:t>{xml_escape(text)}</a:t>\n'
        f'            </a:r>\n'
        f'          </a:p>'
    )


def para_reveal(text: str) -> str:
    """Italic paragraph for click-to-reveal answer boxes."""
    return (
        f'          <a:p>\n'
        f'            <a:r>\n'
        f'              <a:rPr lang="en-GB" sz="2000" i="1" dirty="0">\n'
        f'                <a:solidFill><a:schemeClr val="tx1"/></a:solidFill>\n'
        f'              </a:rPr>\n'
        f'              <a:t>{xml_escape(text)}</a:t>\n'
        f'            </a:r>\n'
        f'          </a:p>'
    )


def bullets_xml(items: list) -> str:
    """Build multiple bullet paragraphs followed by an empty end paragraph."""
    paras = [para_bullet(item) for item in items]
    paras.append('          <a:p><a:endParaRPr lang="en-GB" dirty="0"/></a:p>')
    return "\n".join(paras)


