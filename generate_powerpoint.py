def strip_json_fences(text: str) -> str:
    """Remove markdown code fences if Claude wrapped output in them."""
    text = re.sub(r"^```(?:json)?\s*", "", text.strip())
    text = re.sub(r"\s*```$", "", text)
    return text.strip()


def generate_outline(client, topic_code: str, topic_title: str,
                     research_path: Path, max_slides: int = 25) -> dict:
    print("  Calling Claude API for outline …")
    research_text = load_research_text(research_path, topic_code)[:MAX_RESEARCH_CHARS]
    spec_text     = extract_spec_section(topic_code)[:10_000]

    msg = client.messages.create(
        model=CLAUDE_MODEL,
        max_tokens=8192,
        system=OUTLINE_SYSTEM,
        messages=[{"role": "user", "content": OUTLINE_PROMPT.format(
            topic_code=topic_code,
            topic_title=topic_title,
            spec_text=spec_text,
            research_text=research_text,
            max_slides=max_slides,
        )}],
    )
    if msg.stop_reason == "max_tokens":
        raise RuntimeError(
            "Outline response was cut off (hit max_tokens limit). "
            "The topic may be unusually large — try splitting it or reducing MAX_RESEARCH_CHARS.")
    outline = json.loads(strip_json_fences(msg.content[0].text))

    # Hard enforcement: trim to max_slides if Claude went slightly over
    slides = outline.get("slides", [])
    if len(slides) > max_slides:
        print(f"  ⚠  Outline had {len(slides)} slides — trimming to {max_slides}")
        outline["slides"] = slides[:max_slides]

    return outline


def review_outline_interactive(outline: dict) -> bool:
    """Display outline and request approval in the terminal."""
    print("\n" + "═" * 65)
    print(f"  OUTLINE: {outline['topic_code']} – {outline['topic_title']}")
    print("═" * 65)
    for s in outline["slides"]:
        print(f"  {s['slide_number']:2d}. [{s['type']}]  {s['title']}")
        if desc := s.get("brief_description"):
            print(f"       {desc}")
    print("═" * 65)

    while True:
        answer = input("\nApprove this outline? [y = yes / n = quit]: ").strip().lower()
        if answer == "y":
            return True
        if answer in ("n", "q"):
            return False


def generate_content(client, outline: dict, research_path: Path) -> dict:
    print("  Calling Claude API for full content (may take a minute) …")
    research_text = load_research_text(
        research_path, outline.get("topic_code"))[:MAX_RESEARCH_CHARS]

    msg = client.messages.create(
        model=CLAUDE_MODEL,
        max_tokens=16384,
        system=CONTENT_SYSTEM,
        messages=[{"role": "user", "content": CONTENT_PROMPT.format(
            research_text=research_text,
            outline_json=json.dumps(outline, indent=2),
        )}],
    )
    if msg.stop_reason == "max_tokens":
        raise RuntimeError(
            "Content response was cut off (hit max_tokens limit). "
            "The outline may have too many slides — try approving a shorter outline, "
            "or reduce MAX_RESEARCH_CHARS.")
    return json.loads(strip_json_fences(msg.content[0].text))


