def duplicate_slide(unpacked_dir: Path, source_filename: str, next_slide_id: int):
    """
    Duplicate a template slide from Palette.pptx into the working directory.
    next_slide_id must be provided by the caller (tracked externally to avoid
    re-reading stale presentation.xml during a batch of duplications).
    Returns (new_filename, new_rel_id).
    """
    slides_dir = unpacked_dir / "ppt" / "slides"
    rels_dir   = slides_dir / "_rels"

    # Next available slide number (from filesystem, always accurate)
    existing = [
        int(m.group(1))
        for f in slides_dir.glob("slide*.xml")
        if (m := re.match(r"slide(\d+)\.xml", f.name))
    ]
    next_num  = max(existing) + 1 if existing else 1
    dest_name = f"slide{next_num}.xml"

    # Copy slide XML
    shutil.copy2(slides_dir / source_filename, slides_dir / dest_name)

    # Copy + clean up .rels (drop notes slide references)
    src_rels  = rels_dir / f"{source_filename}.rels"
    dest_rels = rels_dir / f"{dest_name}.rels"
    if src_rels.exists():
        shutil.copy2(src_rels, dest_rels)
        content = dest_rels.read_text(encoding="utf-8")
        content = re.sub(
            r'\s*<Relationship[^>]*Type="[^"]*notesSlide"[^>]*/>\s*', "\n", content)
        dest_rels.write_text(content, encoding="utf-8")

    # Register in [Content_Types].xml
    ct_path = unpacked_dir / "[Content_Types].xml"
    ct = ct_path.read_text(encoding="utf-8")
    if f"/ppt/slides/{dest_name}" not in ct:
        ct = ct.replace(
            "</Types>",
            f'  <Override PartName="/ppt/slides/{dest_name}" '
            f'ContentType="application/vnd.openxmlformats-officedocument.'
            f'presentationml.slide+xml"/>\n</Types>')
        ct_path.write_text(ct, encoding="utf-8")

    # Add relationship to presentation.xml.rels
    pres_rels_path = unpacked_dir / "ppt" / "_rels" / "presentation.xml.rels"
    pres_rels = pres_rels_path.read_text(encoding="utf-8")
    rid_nums  = [int(m) for m in re.findall(r'Id="rId(\d+)"', pres_rels)]
    new_rid   = f"rId{max(rid_nums) + 1 if rid_nums else 1}"
    if f"slides/{dest_name}" not in pres_rels:
        new_rel = (
            f'<Relationship Id="{new_rid}" '
            f'Type="http://schemas.openxmlformats.org/officeDocument/'
            f'2006/relationships/slide" '
            f'Target="slides/{dest_name}"/>')
        pres_rels = pres_rels.replace(
            "</Relationships>", f"  {new_rel}\n</Relationships>")
        pres_rels_path.write_text(pres_rels, encoding="utf-8")

    return dest_name, new_rid


def update_slide_id_list(unpacked_dir: Path,
                         output_slides: list[tuple[int, str]]) -> None:
    """
    Rewrite the <p:sldIdLst> in presentation.xml to include ONLY
    the output slides (removing the original 13 template slides).
    output_slides: list of (slide_id, rel_id) tuples in presentation order.
    """
    pres_path = unpacked_dir / "ppt" / "presentation.xml"
    pres_xml  = pres_path.read_text(encoding="utf-8")

    new_entries = "\n".join(
        f'      <p:sldId id="{sid}" r:id="{rid}"/>'
        for sid, rid in output_slides
    )
    new_lst = f"    <p:sldIdLst>\n{new_entries}\n    </p:sldIdLst>"

    pres_xml = re.sub(
        r"<p:sldIdLst>.*?</p:sldIdLst>",
        new_lst,
        pres_xml,
        flags=re.DOTALL,
    )
    pres_path.write_text(pres_xml, encoding="utf-8")


