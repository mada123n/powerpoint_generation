def extract_research_section(research_path: Path, topic_code: str) -> str | None:
    """
    Extract only the portion of a .docx research document relevant to topic_code.

    Collects all paragraphs under headings whose text starts with topic_code or
    any of its sub-headings (e.g. for '4.1.1' this captures '4.1.1', '4.1.1.1',
    '4.1.1.2', etc.).  Stops as soon as a heading with a *different* numbered
    topic code is encountered (e.g. '4.1.2').

    Non-numbered headings (e.g. 'Key facts', 'Required practical') that appear
    inside the section are included — they are sub-sections of the topic, not
    markers of a new topic.

    Returns None if python-docx is unavailable or no matching headings are found,
    in which case the caller falls back to reading the full document.
    """
    try:
        from docx import Document
    except ImportError:
        return None

    try:
        escaped = re.escape(topic_code)
        # Matches "4.1.1", "4.1.1.1", "4.1.1.2", etc.
        topic_re     = re.compile(rf'^\s*{escaped}(\.\d+)*\b')
        # Matches any numbered heading (used to detect the start of a different topic)
        any_topic_re = re.compile(r'^\s*\d+\.\d+')

        doc = Document(research_path)
        collected = []
        in_section = False

        for para in doc.paragraphs:
            is_heading = para.style.name.startswith("Heading")
            text = para.text.strip()
            if not text:
                continue

            if is_heading:
                if topic_re.match(text):
                    in_section = True
                    collected.append(f"\n{text}\n")
                elif in_section and any_topic_re.match(text):
                    # Hit the next numbered topic — we're done
                    break
                elif in_section:
                    # A non-numbered sub-heading still inside our section
                    collected.append(f"\n{text}\n")
            elif in_section:
                collected.append(text)

        return "\n".join(collected) if collected else None

    except Exception as e:
        print(f"  ⚠  Could not parse research document ({e}); falling back to full document")
        return None


def load_research_text(research_path: Path, topic_code: str = None) -> str:
    """
    Convert research document to plain text for feeding to the Claude API.

    If topic_code is provided and the file is a .docx, attempts to extract only
    the section relevant to that topic (e.g. all headings/content under 4.1.1.*).
    Falls back to reading the full document if extraction finds nothing.
    """
    if research_path.suffix.lower() in (".docx", ".doc"):
        # Attempt targeted section extraction first
        if topic_code:
            section = extract_research_section(research_path, topic_code)
            if section:
                print(f"  📖 Extracted {len(section):,} chars for topic "
                      f"{topic_code} from research doc")
                return section
            else:
                print(f"  ⚠  No headings matching '{topic_code}' found in research doc "
                      f"— using full document")

        # Fall back to full pandoc conversion
        try:
            result = subprocess.run(
                ["pandoc", str(research_path), "-t", "plain", "--wrap=none"],
                capture_output=True, text=True, encoding="utf-8", errors="replace"
            )
        except FileNotFoundError:
            raise RuntimeError(
                "pandoc is not installed or not on PATH. "
                "Install it from https://pandoc.org/installing.html and try again."
            )
        if result.returncode != 0:
            raise RuntimeError(f"pandoc failed: {result.stderr}")
        return result.stdout

    return research_path.read_text(encoding="utf-8", errors="replace")


def extract_spec_section(topic_code: str) -> str:
    """Extract the relevant section from the AQA spec PDF."""
    try:
        import pdfplumber
        text_pages = []
        in_section = False
        # Keep local validation for callers that use this helper outside main().
        if not re.fullmatch(TOPIC_CODE_PATTERN, topic_code):
            print(
                f"  ⚠  Topic code '{topic_code}' is malformed; expected format like "
                f"'4.1.1' or '4.1.1.1'. Returning empty spec text."
            )
            return ""
        parts = topic_code.split(".")
        # Pattern for next sibling at the same depth.
        # e.g. 4.1.1   → matches 4.1.2, 4.1.3 …  (stops at next top-level sub-topic)
        #      4.1.1.1 → matches 4.1.1.2, 4.1.1.3 … (stops within the parent topic only)
        parent_prefix = re.escape(".".join(parts[:-1]))
        next_section_re = re.compile(rf'{parent_prefix}\.\d+')

        with pdfplumber.open(SPEC_PDF_PATH) as pdf:
            for page in pdf.pages:
                page_text = page.extract_text() or ""
                if topic_code in page_text:
                    in_section = True
                if in_section:
                    text_pages.append(page_text)
                    # Stop when we hit the NEXT topic at the same level
                    hits = next_section_re.findall(page_text)
                    hits = [h for h in hits if h != topic_code]
                    if hits and len(text_pages) > 1:
                        break

        return "\n".join(text_pages)
    except Exception as e:
        print(f"  ⚠  Could not extract spec section ({e}); continuing without it.")
        return ""


