def pptx_unpack(src: Path, dest: Path) -> None:
    """Extract a PPTX zip and pretty-print its XML files for string manipulation."""
    dest.mkdir(parents=True, exist_ok=True)
    with zipfile.ZipFile(src, "r") as zf:
        zf.extractall(dest)
    for xml_file in list(dest.rglob("*.xml")) + list(dest.rglob("*.rels")):
        try:
            raw = xml_file.read_bytes()
            dom = xml.dom.minidom.parseString(raw)
            xml_file.write_bytes(dom.toprettyxml(indent="  ", encoding="utf-8"))
        except Exception:
            pass  # Leave malformed XML untouched


def pptx_pack(src: Path, dest: Path) -> None:
    """Re-zip an unpacked PPTX directory back into a .pptx file.
    [Content_Types].xml must be the first entry per the OOXML spec."""
    dest.parent.mkdir(parents=True, exist_ok=True)
    all_files = [f for f in src.rglob("*") if f.is_file()]
    # Sort so [Content_Types].xml is always written first
    ct_file = src / "[Content_Types].xml"
    ordered = ([ct_file] if ct_file in all_files else []) + \
              [f for f in all_files if f != ct_file]
    with zipfile.ZipFile(dest, "w", zipfile.ZIP_DEFLATED) as zf:
        for f in ordered:
            zf.write(f, f.relative_to(src))


def pptx_clean(unpacked_dir: Path) -> None:
    """
    Remove slides not listed in sldIdLst plus their orphaned rels and media files.
    Mirrors the behaviour of the Cowork clean.py script.
    """
    pres_path      = unpacked_dir / "ppt" / "presentation.xml"
    pres_rels_path = unpacked_dir / "ppt" / "_rels" / "presentation.xml.rels"
    slides_dir     = unpacked_dir / "ppt" / "slides"
    rels_dir       = slides_dir / "_rels"

    if not pres_path.exists():
        return

    # Slides referenced by the current sldIdLst
    pres_content    = pres_path.read_text(encoding="utf-8")
    referenced_rids = set(re.findall(r'<p:sldId[^>]*r:id="([^"]+)"', pres_content))

    rid_to_slide: dict[str, str] = {}
    if pres_rels_path.exists():
        for m in re.finditer(
                r'<Relationship[^>]*Id="([^"]+)"[^>]*Target="slides/([^"]+)"',
                pres_rels_path.read_text(encoding="utf-8")):
            rid_to_slide[m.group(1)] = m.group(2)

    referenced_slides = {rid_to_slide[r] for r in referenced_rids if r in rid_to_slide}

    # Delete orphaned slide XML + rels
    removed: list[str] = []
    for slide_file in slides_dir.glob("slide*.xml"):
        if slide_file.name not in referenced_slides:
            slide_file.unlink()
            removed.append(slide_file.name)
            rels_file = rels_dir / f"{slide_file.name}.rels"
            if rels_file.exists():
                rels_file.unlink()

    if not removed:
        return

    # Strip orphaned entries from presentation.xml.rels
    if pres_rels_path.exists():
        rels_text = pres_rels_path.read_text(encoding="utf-8")
        for name in removed:
            rels_text = re.sub(
                rf'\s*<Relationship[^>]*Target="slides/{re.escape(name)}"[^/]*/>\s*',
                "\n", rels_text)
        pres_rels_path.write_text(rels_text, encoding="utf-8")

    # Strip orphaned Override entries from [Content_Types].xml
    ct_path = unpacked_dir / "[Content_Types].xml"
    if ct_path.exists():
        ct_text = ct_path.read_text(encoding="utf-8")
        for name in removed:
            ct_text = re.sub(
                rf'\s*<Override[^>]*PartName="/ppt/slides/{re.escape(name)}"[^/]*/>\s*',
                "\n", ct_text)
        ct_path.write_text(ct_text, encoding="utf-8")

    # Remove media files no longer referenced by any remaining slide
    referenced_media: set[str] = set()
    if rels_dir.exists():
        for rels_file in rels_dir.glob("*.rels"):
            for m in re.finditer(r'Target="\.\./media/([^"]+)"',
                                 rels_file.read_text(encoding="utf-8")):
                referenced_media.add(m.group(1))

    media_dir = unpacked_dir / "ppt" / "media"
    if media_dir.exists():
        for f in media_dir.glob("*"):
            if f.is_file() and f.name not in referenced_media:
                f.unlink()


